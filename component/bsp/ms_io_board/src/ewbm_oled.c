/**
  * @brief  Initialize the OLED module and clear
  * @retval  If success, return EWBMOK value
  */
int OLED_Init()
{
    int ret;
    gSpiHandle.bank = SPI_BANK_1;
    ret = SPI_Init(&gSpiHandle);

    gSpiHandle.dataFormat = SPI_DSS_08_BIT_DATA;
    SPI_DataSize(&gSpiHandle);

    gGpioHandleDC.bank = GPIO_BANK_1;
    GPIO_Init(&gGpioHandleDC);

    gGpioHandleRES.bank = GPIO_BANK_2;
    GPIO_Init(&gGpioHandleRES);

    GPIO_SetDir(&gGpioHandleDC, OLED_DC, 0, SET);
    GPIO_SetDir(&gGpioHandleRES, OLED_RES, 0, SET);

    GPIO_WritePin(&gGpioHandleRES, OLED_RES, SET);
    GPIO_WritePin(&gGpioHandleDC, OLED_DC, RESET);

    write_byte(0xAE, OLED_CMD);  // Turn off oled panel
    write_byte(0x00, OLED_CMD);  // Set low column address
    write_byte(0x10, OLED_CMD);  // Set high column address
    write_byte(0x40, OLED_CMD);  // Set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)
    write_byte(0x81, OLED_CMD);  // Set contrast control register
    write_byte(0xCF, OLED_CMD);  // Set SEG Output Current Brightness
    write_byte(0xA1, OLED_CMD);  // Set SEG/Column Mapping
    write_byte(0xC0, OLED_CMD);  // Set COM/Row Scan Direction
    write_byte(0xA6, OLED_CMD);  // Set normal display
    write_byte(0xA8, OLED_CMD);  // Set multiplex ratio(1 to 64)
    write_byte(0x3f, OLED_CMD);  // 1/64 duty
    write_byte(0xD3, OLED_CMD);  // Set display offset Shift Mapping RAM Counter (0x00~0x3F)
    write_byte(0x00, OLED_CMD);  // Not offset
    write_byte(0xd5, OLED_CMD);  // Set display clock divide ratio/oscillator frequency
    write_byte(0x80, OLED_CMD);  // Set divide ratio, Set Clock as 100 Frames/Sec
    write_byte(0xD9, OLED_CMD);  // Set pre-charge period
    write_byte(0xF1, OLED_CMD);  // Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
    write_byte(0xDA, OLED_CMD);  // Set com pins hardware configuration
    write_byte(0x12, OLED_CMD);  // (Common signal pad configuration)
    write_byte(0xDB, OLED_CMD);  // Set vcomh
    write_byte(0x40, OLED_CMD);  // Set VCOM Deselect Level
    write_byte(0x20, OLED_CMD);  // Set Page Addressing Mode (0x00/0x01/0x02)
    write_byte(0x02, OLED_CMD);  // (0x02)
    write_byte(0x8D, OLED_CMD);  // Set Charge Pump enable/disable
    write_byte(0x14, OLED_CMD);  // Set(0x10) disable
    write_byte(0xA4, OLED_CMD);  // Disable Entire Display On (0xa4/0xa5)
    write_byte(0xA6, OLED_CMD);  // Disable Inverse Display On (0xa6/a7)
    write_byte(0xAF, OLED_CMD);  // Turn on oled panel

    OLED_ClearScreen(0);
    OLED_Invalidate();

    return ret;
}

/**
  * @brief  Deinitialize the OLED module
  * @retval  If success, return EWBMOK value
  */
int OLED_DeInit()
{
    int ret;
    ret = SPI_DeInit(&gSpiHandle);
    memset(&gSpiHandle, 0x00, sizeof(gSpiHandle));
    return ret;
}

