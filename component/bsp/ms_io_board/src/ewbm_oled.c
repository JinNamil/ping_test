/**
  * @brief  Write data or command to the OLED module
  * @param  data: Writing data
  * @param  type: Command or data
  * @retval Pointer to base address
  */
static void write_byte(uint8_t data, uint8_t type)
{
    if(type == OLED_DAT) {
        GPIO_WritePin(&gGpioHandleDC, OLED_DC, SET);
    } else {
        GPIO_WritePin(&gGpioHandleDC, OLED_DC, RESET);
    }
    SPI_Write(&gSpiHandle, data);
}

